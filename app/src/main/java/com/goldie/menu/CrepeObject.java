package com.goldie.menu;

public class CrepeObject {
    private boolean darkChocolate, whiteChocolate;
    private boolean strawberry, banana, blueberry, gummybears, oreo, whippedcream, cream, sprinklers, darkChocolateTop, whiteChocolateTop;


    public CrepeObject(){
    }

    public boolean isDarkChocolate() {
        return darkChocolate;
    }

    public boolean isCream() {
        return cream;
    }

    public void setCream(boolean cream) {
        this.cream = cream;
    }

    public void setDarkChocolate(boolean darkChocolate) {
        this.darkChocolate = darkChocolate;
    }

    public boolean isWhiteChocolate() {
        return whiteChocolate;
    }

    public void setWhiteChocolate(boolean whiteChocolate) {
        this.whiteChocolate = whiteChocolate;
    }

    public boolean isStrawberry() {
        return strawberry;
    }

    public void setStrawberry(boolean strawberry) {
        this.strawberry = strawberry;
    }

    public boolean isBanana() {
        return banana;
    }

    public void setBanana(boolean banana) {
        this.banana = banana;
    }

    public boolean isBlueberry() {
        return blueberry;
    }

    public void setBlueberry(boolean blueberry) {
        this.blueberry = blueberry;
    }

    public boolean isGummybears() {
        return gummybears;
    }

    public void setGummybears(boolean gummybears) {
        this.gummybears = gummybears;
    }

    public boolean isOreo() {
        return oreo;
    }

    public void setOreo(boolean oreo) {
        this.oreo = oreo;
    }

    public boolean isWhippedcream() {
        return whippedcream;
    }

    public void setWhippedcream(boolean whippedcream) {
        this.whippedcream = whippedcream;
    }

    public boolean isSprinklers() {
        return sprinklers;
    }

    public void setSprinklers(boolean sprinklers) {
        this.sprinklers = sprinklers;
    }

    public boolean isDarkChocolateTop() {
        return darkChocolateTop;
    }

    public void setDarkChocolateTop(boolean darkChocolateTop) {
        this.darkChocolateTop = darkChocolateTop;
    }

    public boolean isWhiteChocolateTop() {
        return whiteChocolateTop;
    }

    public void setWhiteChocolateTop(boolean whiteChocolateTop) {
        this.whiteChocolateTop = whiteChocolateTop;
    }
}
